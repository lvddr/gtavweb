                    <p class="about">"Post Traumatic Stress Disorder at its very shallowest" An experimental surrealist film, entirely self-financed by Dreyfuss at the height of his drug use. Universally lambasted by critics in the 1970s, "Bury Me' was resurrected and misinterpreted as cool by nerds and hipsters in the early 2000s and is now widely acclaimed as a classic. Badger McGinnis, played by a young Bill Anthony, is a Vietnam veteran who lives a hermitic existence in a remote New Mexico town. He spends his days digging a hole in his front yard, which was intended to be a Sisyphean allegory for his inability to process or move on from the horrors of war. However, it's essentially just 40 minutes of a man silently digging a hole interspersed with flashbacks of harrowing village massacres; featuring a lot of Koreans, Chinese and Japanese actors playing Vietnamese families. About halfway through the movie, a foul-mouthed paraplegic woman called Jenny shows up who manages to connect with Badger and bring him out of his shell. After they take acid and have awkward sex in the hole — a bizarre scene in which Badger's penis intermittently turns into a bayonet — Jenny reveals herself to be a marine from his old unit who is undergoing gender reassignment treatment. At the denouement, we learn that Badger never made it out of Vietnam alive and the entire movie has been one long 120-minute death hallucination. The film ends with Badger walking down into his hole, where we see the flames of hell burning in the distance. His final word, and the only line of dialogue, is "Simon", which is never explained but has been over-analysed in graduate media studies theses ever since.
